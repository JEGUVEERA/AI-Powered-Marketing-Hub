// utils/sentimentUtils.ts

// Function to detect sentiment
export function detectSentiment(text: string): "Positive" | "Negative" | "Neutral" {
  if (text.includes("happy")) return "Positive";
  if (text.includes("sad")) return "Negative";
  return "Neutral";
}

// Function to detect dominant emotion
export function detectDominantEmotion(text: string): string {
  return text.includes("excited") ? "Excitement" : "Neutral";
}

// Function to extract keywords from text
export function extractKeywords(text: string): string[] {
  return text.split(" ").filter((word) => word.length > 3);  // Example logic
}

// Function to get an emoji based on sentiment or emotion
export function getEmoji(sentimentOrEmotion: string): string {
  if (sentimentOrEmotion === "Positive") return "😊";
  if (sentimentOrEmotion === "Negative") return "😞";
  return "😐";
}

// Function to analyze sentiment and emotion
export function analyzeSentimentAndEmotion(text: string) {
  const sentiment = detectSentiment(text);
  const emotion = detectDominantEmotion(text);
  const keywords = extractKeywords(text);
  const emoji = getEmoji(sentiment);

  return {
    sentiment,
    emotion,
    keywords,
    emoji,
  };
}
